def generate_goals(employee_role: str, department: str, top_k=5) -> list:
    """
    Мок-версия генерации целей без ChromaDB и OpenAI.
    Возвращает предопределённые цели, подходящие по роли и департаменту.
    """
    
    # Простая логика подбора целей под роль и департамент
    role_lower = employee_role.lower()
    dept_lower = department.lower()
    
    filtered_goals = []
    
    for goal in MOCK_GOALS:
        # Подбираем цели под роль
        if "it" in dept_lower or "разработчик" in role_lower or "инженер" in role_lower:
            if "AI" in goal["goal_text"] or "мониторинга" in goal["goal_text"] or "миграции" in goal["goal_text"]:
                filtered_goals.append(goal)
        elif "hr" in dept_lower or "кадр" in dept_lower or "персонал" in dept_lower:
            if "наставничества" in goal["goal_text"] or "сотрудников" in goal["goal_text"]:
                filtered_goals.append(goal)
        else:
            filtered_goals.append(goal)
    
    # Если ничего не подошло, берём первые 3
    if not filtered_goals:
        filtered_goals = MOCK_GOALS[:3]
    
    # Возвращаем 3-5 целей
    return filtered_goals[:min(top_k, len(filtered_goals))]